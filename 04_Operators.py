# Arithmetic_Operators
num1 = 3
num2 = 4

print("num1 + num2 = " , num1 + num2)
print("num1 - num2 = " , num1 - num2)
print("num1 / num2 = " , num1 // num2)
print("num1 * num2 = " , num1 ** num2)
# print("num1 ++ num2" , num1 ++ num2)

